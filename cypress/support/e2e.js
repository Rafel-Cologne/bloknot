// ============================================================================
// cypress/support/e2e.js
// ============================================================================
// Файл поддержки для Cypress
//
// Этот файл загружается перед каждым тестом и содержит:
// - Кастомные команды
// - Конфигурацию
// - Глобальные хуки (before, after и т.д.)
// ============================================================================

// Импортируем команды для работы с cy.intercept
// Это не обязательно, но помогает в IDE с автодополнением
import 'cypress-network-idle';

// ========================================================================
// ГЛОБАЛЬНЫЕ ПЕРЕХВАТЧИКИ (Global Hooks)
// ========================================================================

// Этот код выполняется ДО ВСЕХ ТЕСТОВ
before(function () {
  cy.log('🚀 Начинаем тестирование AI Translator & Critic');
});

// Этот код выполняется ПОСЛЕ КАЖДОГО ТЕСТА
afterEach(function () {
  // Очищаем localStorage и sessionStorage после каждого теста
  cy.clearLocalStorage();
});

// ========================================================================
// ОТКЛЮЧЕНИЕ КОНТРОЛЬНЫХ ОШИБОК (Uncaught Exceptions)
// ========================================================================

// Иногда браузер может выбросить необработанное исключение в консоли.
// Мы можем его игнорировать для некритичных ошибок.
Cypress.on('uncaught:exception', (err, runnable) => {
  // Если сообщение об ошибке содержит определённый текст, игнорируем
  if (
    err.message.includes('ResizeObserver loop limit exceeded') ||
    err.message.includes('Network request failed')
  ) {
    // Возвращаем false, чтобы тест не считался провальным
    return false;
  }
  
  // Для всех остальных ошибок выбрасываем их (тест падает)
  throw err;
});

// ========================================================================
// КАСТОМНЫЕ КОМАНДЫ (Custom Commands)
// ========================================================================

// Создаём кастомную команду для логирования
Cypress.Commands.add('logMessage', (message) => {
  cy.log(`📝 ${message}`);
});

// Кастомная команда для заполнения формы
Cypress.Commands.add('fillTranslationForm', (text, language) => {
  cy.get('textarea[name="text"]').clear().type(text);
  cy.get('select[name="language"]').select(language);
});

// Кастомная команда для нажатия кнопки перевода
Cypress.Commands.add('clickTranslateButton', () => {
  cy.get('button').contains('Перевести').click();
});

// Кастомная команда для нажатия кнопки оценки
Cypress.Commands.add('clickEvaluateButton', () => {
  cy.get('button').contains('Оценить при помощи LLM-as-a-Judge').click();
});

// ========================================================================
// ПЕРЕМЕННЫЕ ОКРУЖЕНИЯ
// ========================================================================

// Устанавливаем переменные, которые можно использовать во всех тестах
Cypress.env('apiUrl', 'https://openrouter.ai/api/v1/chat/completions');
Cypress.env('appUrl', 'http://localhost:5000');
